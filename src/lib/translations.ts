// Helper function to get translations
export function getTranslation(language: string, key: string): string {
  const keys = key.split('.');
  let result: Record<string, unknown> = translations[language as keyof typeof translations];
  
  for (const k of keys) {
    if (result && typeof result === 'object' && k in result) {
      const value = result[k as keyof typeof result];
      if (typeof value === 'object' && value !== null) {
        result = value as Record<string, unknown>;
      } else {
        return value as string;
      }
    } else {
      return key; // Return the key if translation not found
    }
  }
  
  return typeof result === 'string' ? result : key;
}

// Create a hook for using translations
export const useTranslations = (language: "en" | "bg") => {
  return {
    t: (key: string): string | Record<string, unknown> => getTranslation(language, key),
  };
}; 